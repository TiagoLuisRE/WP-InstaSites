<h3>Damage Protection: Protection and Peace of Mind during your Vacation</h3>
<p>Accidental Rental Damage Protection (sometimes also referred to as "Damage Deposit Waiver") is an insurance plan that provides coverage for accidental damages to your vacation rental property during your stay, in place of a security deposit.  Accidental Rental Damage Protection shields you from having to pay out for any damages you accidentally cause to your rental. </p>
<ul>
<li><strong>Automatic Coverage.</strong> Accidental Rental Damage Protection is automatically included when you book your reservation, and saves you from a big check, cash or tying up your credit card with a hefty authorization while you are on vacation.</li>

<li><strong>What's covered?</strong> The plan provides protection up to the amount of coverage per stay and is valid for the entire stay. With this plan, you and your traveling companions will not be obligated to pay for damage or theft to the contents of the rental unit you occupy.  And, the cost and repair of covered accidental damage will be handled between the rental property and the insurance company. </li>

<li><strong>What's not covered?</strong> Intentional acts of a guest and traveling companions; gross negligence or willful conduct; any loss, if a guest or traveling companions do not take reasonable and prudent measures to protect the residence; any loss, if the guest does not report the damage to residence rental management staff by the time of vacating the residence; normal wear and tear; theft without forced entry; damage or loss caused by any pet or other animal that was not allowed onto the premises under the Rental Agreement.</li>
</ul>
<strong>For the specific details on your coverage, please refer to the email you will receive after your reservation is confirmed.</strong>

<h3>Travel Protection: Protect your Vacation from Cancellation, Delays and Emergencies</h3>
<p>Travel Protection (sometimes also referred to as "Cancellation Insurance") is an optional but recommended insurance plan that provides a wide array of significant levels of coverage for when things go wrong before and during your vacation.  Protect your trip with confidence!</p>

<p><strong>What's covered?</strong> Travel Protection is for sudden, unforeseen circumstances that may arise, that could force you to cancel or interrupt your vacation plans.  With this plan you protect the money you have invested in nonrefundable deposits and prepayments that can add up to hundreds or thousands of dollars.  This plan also covers accident and medical costs that in many instances may not be covered by health insurance.  While rare, this plan also covers losses from hurricanes, acts of terrorism and other events and situations, all outlined in detail in your policy. Here are some common scenarios where Travel Protection offers benefits.</p>

<ul>
<li>Trip Cancellations due to unforeseen circumstances</li>
<li>Trip Interruptions</li>
<li>Certain Travel &amp; Baggage Delays</li>
<li>Certain Medical &amp; Dental Expenses</li>
<li>Medical Evacuation &amp; Repatriation</li>
</ul>

